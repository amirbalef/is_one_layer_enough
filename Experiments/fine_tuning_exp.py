"""
fine_tuning_exp.py
------------------
Fine-tune per-layer decoders on synthetic prior data generated by a
tabular in-context learning model (e.g. TabPFN, TabICL).

Each transformer layer gets its own decoder head, trained independently
with AdamW. Training progress is logged to Weights & Biases.

Typical usage
-------------
python fine_tuning_exp.py --config tabpfn_v1.config_finetuning_decoder
"""

import argparse
import copy
import math
import os
import pickle
import warnings

import torch
import torch.nn as nn
import wandb
from tqdm import tqdm
from torch.utils.data import DataLoader
from ticla.priors.tabicl_v1.genload import LoadPriorDataset

from util import get_classifier_config_list

warnings.warn = lambda *a, **kw: None
warnings.filterwarnings("ignore")


# ------CLI ------------
def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Fine-tune per-layer decoders on prior data.",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument(
        "--config", type=str, default="tabpfn_v1.config_finetuning_decoder",
        help="Dotted config module path (e.g. 'tabpfn_v1.config_finetuning_decoder').",
    )
    return parser.parse_args()


# ---------- Loading priors (synthtic datasets) -------------------

def build_dataloader(config: dict, device: str) -> DataLoader:
    """
    Construct a DataLoader backed by a ``LoadPriorDataset``.

    The dataset handles its own internal batching, so ``batch_size=None``
    is passed to the DataLoader to avoid double-batching.
    """
    dataset = LoadPriorDataset(
        data_dir=config["prior_data_dir"],
        batch_size=config["prior_batch_size"],
        ddp_world_size=1,
        ddp_rank=0,
        start_from=0,
        delete_after_load=False,
        device="cpu",
    )
    return DataLoader(
        dataset,
        batch_size=None,
        shuffle=False,
        num_workers=1,
        prefetch_factor=4,
        pin_memory=False,
        pin_memory_device=device,
    )


# ------------- Loading Model Utils -------------------

def resolve_number_of_layers(config: dict) -> int:
    """Infer the number of transformer layers from the model config."""
    if config["model_name"] == "tabicl":
        return len(config["model_parameters"]["predictor_layers_info"])
    return len(config["model_parameters"]["layers_info"])


def build_decoders_and_optimizers(
    model_decoder: nn.Module, n_layers: int
) -> tuple[list[nn.Module], list[torch.optim.Optimizer]]:
    """
    Create one independent decoder + AdamW optimizer per layer (plus the final layer).

    Returns
    -------
    decoders : list of nn.Module, length ``n_layers + 1``
    optimizers : list of Optimizer, same length
    """
    decoders:    list[nn.Module]                = []
    optimizers:  list[torch.optim.Optimizer]    = []

    for _ in range(n_layers + 1):
        decoder   = copy.deepcopy(model_decoder)
        optimizer = torch.optim.AdamW(
            decoder.parameters(), lr=3e-5, weight_decay=1e-4
        )
        decoders.append(decoder)
        optimizers.append(optimizer)

    return decoders, optimizers


# ---------- Training step ---------------

def train_step(
    embeddings:      torch.Tensor,
    targets:         torch.Tensor,
    decoder:         nn.Module,
    optimizer:       torch.optim.Optimizer,
    criterion:       nn.Module,
    train_size:      int,
    needs_transpose: bool,
) -> float:
    """
    Run one gradient update for a single decoder on a single batch.

    Parameters
    ----------
    embeddings : Tensor of shape ``(batch, seq, hidden)``
        Full-sequence embeddings; the test portion starts at index *train_size*.
    targets : Tensor of shape ``(batch, n_test)``
        Ground-truth class indices for the test rows.
    needs_transpose : bool
        Some decoder architectures expect ``(seq, batch, hidden)`` input.

    Returns
    -------
    float
        The scalar loss value for this step (``nan`` if the update was skipped).
    """
    decoder.train()

    test_emb = embeddings[:, train_size:]
    preds    = decoder(test_emb.transpose(0, 1)).transpose(0, 1) if needs_transpose \
               else decoder(test_emb)

    loss = criterion(preds.permute(0, 2, 1), targets.long())

    if not torch.isfinite(loss):
        print("Skipping batch: non-finite loss")
        return float("nan")

    optimizer.zero_grad()
    loss.backward()
    torch.nn.utils.clip_grad_norm_(decoder.parameters(), 1.0)
    optimizer.step()

    return loss.item()


def _prepare_embeddings(clf, config: dict) -> list[torch.Tensor]:
    """
    Retrieve layer embeddings from *clf* and apply optional pre-norm and
    label-token selection.
    """
    embeddings = clf.get_all_layers_embeddings()

    if config.get("decoder_pre_norm", False):
        pre_norm = clf.get_decoder_pre_norm()
        target_device = next(clf.get_decoder().parameters()).device
        embeddings = [
            pre_norm(emb.to(target_device).float()).detach()
            for emb in embeddings
        ]

    if config.get("separate_y_embeddings", False):
        # Keep only the label token (last token in the sequence dimension)
        embeddings = [emb[:, :, -1, :] for emb in embeddings]

    return embeddings


def _log_losses(step: int, avg_loss: list, micro_batch_size: int, training_batch_size: int) -> None:
    """Log per-layer losses to W&B, handling both gradient-accumulation modes."""
    steps = training_batch_size // micro_batch_size

    if steps == 0:
        # Each entry in avg_loss is itself a list (one value per sub-batch)
        n_sub = micro_batch_size // training_batch_size
        for sub_idx in range(n_sub):
            log_res = {"epoch": step * n_sub + sub_idx}
            for layer_idx, layer_losses in enumerate(avg_loss):
                log_res[f"loss_layer_{layer_idx}"] = layer_losses[sub_idx]
            wandb.log(log_res)
    else:
        log_res = {"epoch": step}
        for layer_idx, loss in enumerate(avg_loss):
            log_res[f"loss_layer_{layer_idx}"] = loss
        wandb.log(log_res)


def run_on_micro_batches(
    step:          int,
    clf,
    n_layers:      int,
    batch:         list,
    decoders:      list[nn.Module],
    optimizers:    list[torch.optim.Optimizer],
    criterion:     nn.Module,
    config:        dict,
    device:        str,
) -> list:
    """
    Process one macro-batch as a sequence of micro-batches, accumulate
    embeddings, then run one decoder update per layer.

    Returns
    -------
    list
        Accumulated loss values for this step (appended to the caller's
        ``loss_per_step`` log).
    """
    prior_batch_size   = config["prior_batch_size"]
    micro_batch_size   = config["micro_batch_size"]
    training_batch_size = config["training_batch_size"]
    needs_transpose    = config.get("needs_transpose", False)

    # Materialise any nested tensors
    batch = [t.to_padded_tensor(padding=0.0) if t.is_nested else t for t in batch]

    n_micro_batches = math.ceil(prior_batch_size / micro_batch_size)
    # Transpose: list-of-tensors → list-of-micro-batch tuples
    micro_batches = list(zip(*[torch.split(t, micro_batch_size, dim=0) for t in batch]))

    gradient_acc_steps = training_batch_size // micro_batch_size

    accumulated_embeddings: list[torch.Tensor] = []
    accumulated_targets:    list[torch.Tensor] = []
    step_avg_losses: list = []

    for idx, micro_batch in enumerate(tqdm(micro_batches, desc=f"Step {step}")):
        X, y, _d, seq_len, train_size = micro_batch
        train_size = int(train_size[0])
        y_train    = y[:, :train_size]
        y_test     = y[:, train_size:]

        # Forward pass through the frozen backbone (no gradients needed)
        with torch.no_grad():
            clf.model_inference(X, y_train, train_size)

        embeddings = _prepare_embeddings(clf, config)
        embeddings = torch.stack(embeddings, dim=0)  # (n_layers, batch, seq, hidden)

        accumulated_embeddings.append(embeddings.clone())
        accumulated_targets.append(y_test.clone())

        # Fire a decoder update when the accumulation window is full
        update_now = (gradient_acc_steps == 0) or (idx % gradient_acc_steps == gradient_acc_steps - 1)
        if not update_now:
            continue

        stacked_emb     = torch.cat(accumulated_embeddings, dim=1)   # (n_layers, total_batch, ...)
        stacked_targets = torch.cat(accumulated_targets, dim=0)

        avg_loss: list = []
        for layer_emb, decoder, optimizer in zip(stacked_emb, decoders, optimizers):
            layer_emb       = layer_emb.to(device)
            decoder         = decoder.to(device)
            stacked_targets = stacked_targets.to(device)

            if gradient_acc_steps == 0:
                # Sub-batch loop when training_batch_size < micro_batch_size
                sub_losses = [
                    train_step(
                        layer_emb[b : b + training_batch_size],
                        stacked_targets[b : b + training_batch_size],
                        decoder, optimizer, criterion, train_size, needs_transpose,
                    )
                    for b in range(0, micro_batch_size, training_batch_size)
                ]
                avg_loss.append(sub_losses)
            else:
                loss = train_step(
                    layer_emb, stacked_targets,
                    decoder, optimizer, criterion, train_size, needs_transpose,
                )
                avg_loss.append(loss)

        _log_losses(step, avg_loss, micro_batch_size, training_batch_size)
        step_avg_losses.append(avg_loss)

        accumulated_embeddings = []
        accumulated_targets    = []

    return step_avg_losses


# ------------- Checkpointing  --------------------------

def save_checkpoint(
    config:         dict,
    decoders:       list[nn.Module],
    loss_per_step:  dict,
    step:           int,
) -> None:
    """
    Save decoder weights and the loss log every ``config["save_every_step"]`` steps.
    """
    if (step + 1) % config["save_every_step"] != 0:
        return

    save_dir = config["saving_dir"]
    os.makedirs(save_dir, exist_ok=True)

    for layer_idx, decoder in enumerate(decoders):
        torch.save(decoder.state_dict(), os.path.join(save_dir, f"decoder_layer_{layer_idx}.pth"))

    with open(os.path.join(save_dir, "loss_per_step.pkl"), "wb") as f:
        pickle.dump(loss_per_step, f)

    print(f"  Checkpoint saved to {save_dir} (step {step + 1})")




# ------------- Main ----------------------------------

def main() -> None:
    args = _parse_args()

    # ---- Config ----
    config_path       = "configs." + args.config
    configs, clf_classes = get_classifier_config_list(config_path)
    config            = configs[0]
    clf_class         = clf_classes[0]

    # ---- W&B ----
    wandb.init(
        project="TFM-decoder-finetuning",
        name=config["model"],
        config=config,
        mode=config["wandb_mode"],
    )

    device    = "cuda" if torch.cuda.is_available() else "cpu"
    criterion = nn.CrossEntropyLoss()

    # ---- Data ----
    dataloader = iter(build_dataloader(config, device))

    # ---- Model ----
    clf           = clf_class(**config["model_parameters"], random_state=0)
    n_layers      = resolve_number_of_layers(config)
    decoders, optimizers = build_decoders_and_optimizers(clf.get_decoder(), n_layers)

    print(
        f"Model: {config['model']} | Layers: {n_layers + 1} decoders | "
        f"Device: {device} | Steps: {config['training_max_steps']}"
    )

    # ---- Training loop ----
    loss_per_step: dict = {}

    for step in range(config["training_max_steps"]):
        batch      = next(dataloader)
        step_losses = run_on_micro_batches(
            step, clf, n_layers, batch, decoders, optimizers, criterion, config, device
        )
        loss_per_step[step] = step_losses
        save_checkpoint(config, decoders, loss_per_step, step)


if __name__ == "__main__":
    main()